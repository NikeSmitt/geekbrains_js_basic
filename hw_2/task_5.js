// Реализовать четыре основные арифметические операции в виде функций с двумя параметрами. 
// Обязательно использовать оператор return.


function add(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function mult(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}


let a = 10;
let b = 5;


console.log(add(a, b));
console.log(sub(a, b));
console.log(mult(a, b));
console.log(div(a, b));